document.addEventListener('DOMContentLoaded', function() {
  const heroSlider = document.querySelector('.hero-slider');
  if (!heroSlider) return;

  const slides = heroSlider.querySelectorAll('.hero-slide');
  const dots = heroSlider.querySelectorAll('.hero-dots .dot');
  
  if (slides.length === 0) return;

  let currentSlide = 0;
  let autoSlideInterval;
  const autoSlideDelay = 5000; // 5 saniye

  // Belirli bir slide'Ä± gÃ¶ster
  function showSlide(index) {
    // Slide sÄ±nÄ±rlarÄ±nÄ± kontrol et
    if (index >= slides.length) {
      currentSlide = 0;
    } else if (index < 0) {
      currentSlide = slides.length - 1;
    } else {
      currentSlide = index;
    }

    // TÃ¼m slide'larÄ± gizle ve aktif olanÄ± gÃ¶ster
    slides.forEach((slide, i) => {
      slide.classList.remove('active');
      if (i === currentSlide) {
        slide.classList.add('active');
      }
    });

    // Dot'larÄ± gÃ¼ncelle
    dots.forEach((dot, i) => {
      dot.classList.remove('active');
      if (i === currentSlide) {
        dot.classList.add('active');
      }
    });
  }

  // Sonraki slide'a geÃ§
  function nextSlide() {
    showSlide(currentSlide + 1);
  }

  // Ã–nceki slide'a geÃ§
  function prevSlide() {
    showSlide(currentSlide - 1);
  }

  // Otomatik slide baÅŸlat
  function startAutoSlide() {
    stopAutoSlide(); // Ã–nceki interval'i temizle
    autoSlideInterval = setInterval(nextSlide, autoSlideDelay);
  }

  // Otomatik slide durdur
  function stopAutoSlide() {
    if (autoSlideInterval) {
      clearInterval(autoSlideInterval);
      autoSlideInterval = null;
    }
  }

  // Dot'lara tÄ±klama (Manuel kontrol)
  dots.forEach((dot, index) => {
    dot.addEventListener('click', function() {
      stopAutoSlide();
      showSlide(index);
      startAutoSlide(); // Manuel kontrol sonrasÄ± otomatik devam et
    });
  });

  // Mouse slider Ã¼zerindeyken otomatik geÃ§iÅŸi durdur
  heroSlider.addEventListener('mouseenter', stopAutoSlide);
  heroSlider.addEventListener('mouseleave', startAutoSlide);

  // Touch/swipe desteÄŸi (mobil iÃ§in)
  let touchStartX = 0;
  let touchEndX = 0;

  heroSlider.addEventListener('touchstart', function(e) {
    touchStartX = e.changedTouches[0].screenX;
    stopAutoSlide();
  });

  heroSlider.addEventListener('touchend', function(e) {
    touchEndX = e.changedTouches[0].screenX;
    handleSwipe();
    startAutoSlide();
  });

  function handleSwipe() {
    const swipeThreshold = 50;
    if (touchEndX < touchStartX - swipeThreshold) {
      // Sola kaydÄ±rma - sonraki
      nextSlide();
    }
    if (touchEndX > touchStartX + swipeThreshold) {
      // SaÄŸa kaydÄ±rma - Ã¶nceki
      prevSlide();
    }
  }

  // Klavye ile kontrol (opsiyonel)
  document.addEventListener('keydown', function(e) {
    // Sadece slider gÃ¶rÃ¼nÃ¼rken Ã§alÄ±ÅŸsÄ±n
    if (!heroSlider || !heroSlider.offsetParent) return;
    
    if (e.key === 'ArrowLeft') {
      stopAutoSlide();
      prevSlide();
      startAutoSlide();
    } else if (e.key === 'ArrowRight') {
      stopAutoSlide();
      nextSlide();
      startAutoSlide();
    }
  });

  // Ä°lk slide'Ä± gÃ¶ster ve otomatik kaydÄ±rmayÄ± baÅŸlat
  showSlide(0);
  startAutoSlide();
});

